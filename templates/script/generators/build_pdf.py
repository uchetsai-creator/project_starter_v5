SPEC_SECTIONS = {"introduction", "design", "build", "deployment"}

def find_allowed_files(docs_dir, strings, project_type=None, content="full"):
    """Return (rel, abs_path, section_label) for every allowlisted file that exists
    and applies to the given project_type. log-*.md files are intentionally excluded.

    project_type: frozenset of type strings (supports hybrid projects, e.g. {"data-pipeline","web-app"}),
                  or None to include all entries regardless of type (backward-compatible default).
    content: "full" (default) — all chapters; "spec" — Introduction, Design, Build, Deployment only.
    """
    result = []
    seen = set()

    for section_key, rel, types in PDF_ALLOWLIST:
        if content == "spec" and section_key not in SPEC_SECTIONS:
            continue
        if project_type is not None and not (project_type & types):
            continue
        abs_path = os.path.join(docs_dir, rel)
        section_label = strings["sections"][section_key]
        if os.path.exists(abs_path):
            result.append((rel, abs_path, section_label))
            seen.add(rel)
        else:
            if project_type is not None:
                print(f"Warning: expected file not found for "
                      f"{'+'.join(sorted(project_type))}, skipping: {rel}")
            # silently skip files that don't exist when no project type is set

    def _should_scan(pattern):
        if project_type is None:
            return True
        return bool(project_type & AUTO_SCAN_TYPES.get(pattern, frozenset()))

    flows_label = strings["sections"]["build"]

    # Insert individual module-data-flow files RIGHT AFTER the module-data-flow.md index entry
    # so they appear within the Build chapter, not after Deployment.
    data_flow_idx = next(
        (i for i, (r, _, _) in enumerate(result) if r == "modules/module-data-flow.md"),
        len(result),
    )
    insert_pos = data_flow_idx + 1
    if _should_scan("modules/*/*-module-data-flow.md"):
        for path in sorted(glob.glob(os.path.join(docs_dir, "modules", "*", "*-module-data-flow.md"))):
            rel = os.path.relpath(path, docs_dir)
            if rel not in seen:
                result.insert(insert_pos, (rel, path, flows_label))
                seen.add(rel)
                insert_pos += 1

    # Insert individual flow files RIGHT AFTER the module-flow.md index entry.
    flow_idx = next(
        (i for i, (r, _, _) in enumerate(result) if r == "modules/module-flow.md"),
        len(result),
    )
    insert_pos = flow_idx + 1
    if _should_scan("modules/*/*-flow.md"):
        for path in sorted(glob.glob(os.path.join(docs_dir, "modules", "*", "*-flow.md"))):
            rel = os.path.relpath(path, docs_dir)
            if rel not in seen:
                result.insert(insert_pos, (rel, path, flows_label))
                seen.add(rel)
                insert_pos += 1

    business_label = strings["sections"]["introduction"]
    if _should_scan("business/*-process.md"):
        for path in sorted(glob.glob(os.path.join(docs_dir, "business", "*-process.md"))):
            rel = os.path.relpath(path, docs_dir)
            if rel not in seen:
                for idx, (r, _, _) in enumerate(result):
                    if r == "business/business-process.md":
                        result.insert(idx + 1, (rel, path, business_label))
                        seen.add(rel)
                        break

    if _should_scan("business/*-object.md"):
        for path in sorted(glob.glob(os.path.join(docs_dir, "business", "*-object.md"))):
            rel = os.path.relpath(path, docs_dir)
            if rel not in seen:
                for idx, (r, _, _) in enumerate(result):
                    if r == "business/business-objects.md":
                        result.insert(idx + 1, (rel, path, business_label))
                        seen.add(rel)
                        break

    design_label = strings["sections"]["design"]
    if _should_scan("specs/prompts/*-prompt.md"):
        for path in sorted(glob.glob(os.path.join(docs_dir, "specs", "prompts", "*-prompt.md"))):
            rel = os.path.relpath(path, docs_dir)
            if rel not in seen:
                for idx, (r, _, _) in enumerate(result):
                    if r == "specs/prompt-library.md":
                        result.insert(idx + 1, (rel, path, design_label))
                        seen.add(rel)
                        break

    # Renumber chapters sequentially (1, 2, 3 …) based on appearance order.
    # This prevents gaps like 1 → 3 → 4 → 6 when spec mode skips chapters.
    seen_labels: list[str] = []
    for _, _, lbl in result:
        if lbl not in seen_labels:
            seen_labels.append(lbl)
    label_map: dict[str, str] = {}
    for i, lbl in enumerate(seen_labels, 1):
        base = re.sub(r"^\d+\.\s+|^[一二三四五六七八九十]+[、.]\s*", "", lbl)
        label_map[lbl] = f"{i}. {base}"
    result = [(rel, abs_path, label_map[lbl]) for rel, abs_path, lbl in result]

    return result


def render_plantuml_block(puml_text, out_svg_path):
    """Render a PlantUML block to SVG using plantuml.jar.
    Returns True on success, False on failure."""
    if not os.path.exists(PLANTUML_JAR):
        print(f"Warning: plantuml.jar not found at {PLANTUML_JAR}")
        print("  Set PLANTUML_JAR env var or place plantuml.jar next to build_pdf.py")
        return False
    with tempfile.NamedTemporaryFile(suffix='.puml', mode='w', delete=False, encoding='utf-8') as f:
        f.write(puml_text)
        tmp_puml = f.name
    try:
        out_svg_dir = os.path.dirname(os.path.abspath(out_svg_path))
        cfg_path = os.path.join(os.path.dirname(PLANTUML_JAR), 'plantuml.cfg')
        cmd = ['java', '-Dfile.encoding=UTF-8', '-jar', PLANTUML_JAR, '-tsvg', '-o', out_svg_dir]
        if os.path.exists(cfg_path):
            cmd += ['-config', cfg_path]
        cmd.append(tmp_puml)
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
        # PlantUML outputs SVG to out_svg_dir using the input filename's stem
        # e.g. /tmp/tmpXXXX.puml → out_svg_dir/tmpXXXX.svg
        tmp_svg = os.path.join(out_svg_dir, os.path.splitext(os.path.basename(tmp_puml))[0] + '.svg')
        if os.path.exists(tmp_svg):
            shutil.move(tmp_svg, out_svg_path)
            return True
        if result.returncode != 0:
            print(f"PlantUML error: {result.stderr[:200]}")
        return False
    except subprocess.TimeoutExpired:
        print("PlantUML render timed out")
        return False
    except FileNotFoundError:
        print("Java not found — install Java to render PlantUML diagrams")
        return False
    finally:
        os.unlink(tmp_puml)


def extract_plantuml_from_file(md_path, svg_cache_dir):
    """Extract all ```plantuml blocks from a markdown file and render them to SVG.
    Returns dict: {diagram_key: svg_path}"""
    with open(md_path, 'r', encoding='utf-8') as f:
        md_text = f.read()

    pairs = {}
    base = os.path.splitext(os.path.basename(md_path))[0]
    blocks = list(re.finditer(r'```plantuml\n([\s\S]*?)```', md_text))

    for idx, m in enumerate(blocks):
        puml_text = m.group(1).strip()
        suffix = '' if len(blocks) == 1 else f'-{idx}'
        key = f'{base}{suffix}'
        svg_path = os.path.join(svg_cache_dir, f'{key}.svg')
        pairs[key] = {'svg': svg_path, 'puml': puml_text, 'md': md_path}

    return pairs


def find_plantuml_diagrams(docs_dir, png_cache_dir, project_type=None, content="full"):
    """Scan all allowlisted markdown files for ```plantuml blocks.
    Renders each block to SVG (with mtime-based caching).
    Returns dict: {diagram_key: {svg, md}}"""
    svg_cache_dir = os.path.join(png_cache_dir, 'plantuml_svg')
    os.makedirs(svg_cache_dir, exist_ok=True)

    all_pairs = {}
    # Collect md files from allowlist + auto-scanned patterns
    md_files = set()
    for section_key, rel, types in PDF_ALLOWLIST:
        if content == "spec" and section_key not in SPEC_SECTIONS:
            continue
        if project_type is not None and not (project_type & types):
            continue
        abs_path = os.path.join(docs_dir, rel)
        if os.path.exists(abs_path):
            md_files.add(abs_path)
    # Also scan auto-included files not in the static allowlist
    for pattern, types in AUTO_SCAN_TYPES.items():
        if project_type is not None and not (project_type & types):
            continue
        for p in glob.glob(os.path.join(docs_dir, pattern)):
            md_files.add(p)

    for md_path in md_files:
        pairs = extract_plantuml_from_file(md_path, svg_cache_dir)
        for key, info in pairs.items():
            svg_path = info['svg']
            puml_text = info['puml']
            md_mtime  = os.path.getmtime(md_path)
            svg_mtime = os.path.getmtime(svg_path) if os.path.exists(svg_path) else -1
            if not os.path.exists(svg_path) or md_mtime > svg_mtime:
                print(f"  Rendering {key}...")
                render_plantuml_block(puml_text, svg_path)
            if os.path.exists(svg_path):
                all_pairs[key] = {'svg': svg_path, 'html': None, 'md': md_path}

    return all_pairs


def find_html_svg_pairs(docs_dir):
    """Find .html files that have a matching .svg (legacy — kept for schema_to_html.py ERD)."""
    pairs = {}
    for html_path in glob.glob(os.path.join(docs_dir, "**", "*.html"), recursive=True):
        svg_path = os.path.splitext(html_path)[0] + ".svg"
        if os.path.exists(svg_path):
            key = os.path.splitext(os.path.basename(html_path))[0]
            pairs[key] = {"html": html_path, "svg": svg_path}
    return pairs


def svg_to_png(svg_path, out_path, max_width=1400, max_height=900):
    """Convert SVG to PNG, scaling to fit within max_width x max_height."""
    tree = ET.parse(svg_path)
    root = tree.getroot()
    vb = root.get('viewBox', '')
    if vb:
        parts = vb.split()
        svg_w = float(parts[2])
        svg_h = float(parts[3])
        scale_w = max_width / svg_w
        scale_h = max_height / svg_h
        scale = min(scale_w, scale_h)
        out_w = int(svg_w * scale)
        out_h = int(svg_h * scale)
    else:
        out_w = max_width
        out_h = None
    cairosvg.svg2png(url=svg_path, write_to=out_path,
                     output_width=out_w, output_height=out_h)
    return out_path


def inject_plantuml_blocks(md_text, rel, docs_dir, html_svg_pairs, png_cache_dir):
    """Replace ```plantuml blocks in markdown with embedded PNG images."""
    base = os.path.splitext(os.path.basename(rel))[0]

    def replace_block(m):
        puml_text = m.group(1).strip()
        # Find matching pre-rendered SVG
        svg_cache_dir = os.path.join(png_cache_dir, 'plantuml_svg')
        # Try to find the svg by scanning rendered pairs
        for key, pair in html_svg_pairs.items():
            if pair.get('md') and os.path.basename(pair['md']) == os.path.basename(rel):
                svg_path = pair['svg']
                if os.path.exists(svg_path):
                    png_name = os.path.splitext(os.path.basename(svg_path))[0] + '.png'
                    png_path = os.path.join(png_cache_dir, png_name)
                    svg_mtime  = os.path.getmtime(svg_path)
                    png_mtime  = os.path.getmtime(png_path) if os.path.exists(png_path) else -1
                    if not os.path.exists(png_path) or svg_mtime > png_mtime:
                        svg_to_png(svg_path, png_path)
                    if os.path.exists(png_path):
                        png_rel = os.path.relpath(png_path, docs_dir)
                        return '![](' + png_rel + ')\n'
        return ''  # fallback: remove the block if no SVG found

    return re.sub(r'```plantuml([\s\S]*?)```', replace_block, md_text)


def inject_diagrams(md_text, rel, docs_dir, html_svg_pairs, png_cache_dir, strings):
    """Inject a rendered PNG + clickable link for diagrams whose target doc matches rel.

    Fixed targets come from DIAGRAM_TARGETS.
    Dynamic targets: a key ending in '-module-data-flow-class' maps to the
    corresponding '[module]-module-data-flow.md' file in flows/.
    """
    for key, pair in html_svg_pairs.items():
        # Resolve target: fixed mapping first, then dynamic module-data-flow-class pattern
        raw_target = DIAGRAM_TARGETS.get(key)
        # raw_target can be:
        #   str  → inject into that one doc
        #   list → inject into each listed doc (diagram appears in multiple sections)
        #   None → resolved dynamically below
        if isinstance(raw_target, list):
            # Only inject into the current doc if it's one of the listed targets
            if rel not in raw_target:
                continue
            target = rel
        elif raw_target is not None:
            target = raw_target
        else:
            target = None
        if target is None:
            # Dynamic: order-module-data-flow-class → modules/order/order-module-data-flow.md
            m = re.match(r'^(.+)-class$', key)
            if m:
                base = m.group(1)  # e.g. order-module-data-flow
                # Extract module name: order-module-data-flow -> order
                module_name = base.replace('-module-data-flow', '')
                target = f"modules/{module_name}/{base}.md"

            # Dynamic: order-flow-sequence → modules/order/order-flow.md
            m2 = re.match(r'^(.+)-sequence$', key)
            if m2:
                base = m2.group(1)  # e.g. order-flow
                module_name = base.replace('-flow', '')
                target = f"modules/{module_name}/{base}.md"

            # Dynamic: order-flow-activity → modules/order/order-flow.md
            # Dynamic: order-create-process-activity → business/order-create-process.md
            m3 = re.match(r'^(.+)-activity$', key)
            if m3:
                base = m3.group(1)
                if base.endswith('-flow'):
                    module_name = base.replace('-flow', '')
                    target = f"modules/{module_name}/{base}.md"
                elif base.endswith('-process'):
                    target = f"business/{base}.md"

            # Dynamic: order-object-state → business/order-object.md
            m4 = re.match(r'^(.+)-state$', key)
            if m4:
                base = m4.group(1)
                if base.endswith('-object'):
                    target = f"business/{base}.md"
                elif base == 'data-model':
                    target = 'specs/data-model.md'

        if target != rel:
            continue

        png_name = f"{key}.png"
        png_path = os.path.join(png_cache_dir, png_name)
        # Regenerate PNG if it does not exist OR if the source SVG is newer than the cache
        svg_mtime = os.path.getmtime(pair["svg"]) if os.path.exists(pair["svg"]) else 0
        png_mtime = os.path.getmtime(png_path)    if os.path.exists(png_path)   else -1
        if not os.path.exists(png_path) or svg_mtime > png_mtime:
            svg_to_png(pair["svg"], png_path)

        png_rel = os.path.relpath(png_path, docs_dir)
        # PlantUML-generated pairs have html=None — skip interactive link
        html_rel = os.path.relpath(pair["html"], docs_dir) if pair.get("html") else None

        # Check if image is tall (height > width) — use full-page display
        img = Image.open(png_path)
        img_w, img_h = img.size
        is_tall = img_h > img_w * 1.5

        link_html = (
                f'<p style="margin-top:8px;"><a href="{html_rel}" style="color:#3182CE;">'
                f'🔗 {strings["diagram_link"]}</a></p>'
            ) if html_rel else ""
        link_md = (
                f"> 🔗 [{strings['diagram_link']}]({html_rel})\n\n"
            ) if html_rel else "\n\n"
        if is_tall:
            block = (
                f'\n\n<div style="page-break-before: always; text-align: center; padding: 24px;">'
                f'<p style="font-weight:bold; color:#2D3748; margin-bottom:8px;">'
                f'{strings["diagram_label"]}: {key}</p>'
                f'<img src="{png_rel}" style="max-width:90%; max-height:90vh; object-fit:contain;"/>'
                f'{link_html}</div>\n\n'
            )
        else:
            block = (
                f"\n\n> **{strings['diagram_label']}: {key}**\n"
                f">\n"
                f"> ![{key} diagram]({png_rel})\n"
                f">\n"
                f"{link_md}"
            )
        # If the doc contains a placement marker <!-- diagram: KEY -->, insert there.
        # Otherwise append to end of document.
        marker = f'<!-- diagram: {key} -->'
        if marker in md_text:
            md_text = md_text.replace(marker, block, 1)
        else:
            md_text += block

    return md_text


def filter_sections(md_text, keep_headings):
    """
    Keep only the specified ## sections (and their content) from a markdown file.
    Everything else — including the file's H1 title and any other sections — is removed.
    keep_headings: list of exact heading strings, e.g. ["## Module Flow Files"]
    """
    lines = md_text.splitlines(keepends=True)
    result = []
    inside_kept = False
    current_level = None

    for line in lines:
        stripped = line.rstrip()

        # Detect headings
        heading_match = re.match(r'^(#{1,6})\s+(.+)$', stripped)
        if heading_match:
            level = len(heading_match.group(1))
            full_heading = stripped

            # Check if this heading is one we want to keep
            if full_heading in keep_headings:
                inside_kept = True
                current_level = level
                result.append(line)
                continue

            # If we hit a heading of same or higher level, stop keeping
            if inside_kept and level <= current_level:
                inside_kept = False
                current_level = None
            # Sub-headings inside a kept section are kept automatically
            # (they have higher level number = lower hierarchy)

        if inside_kept:
            result.append(line)

    return ''.join(result).strip()



def clean_for_pdf(md_text):
    """
    Strip AI-instruction content from markdown before rendering to PDF.

    Removes:
    1. HTML comments <!-- ... -->
    2. Table rows where every cell is a placeholder ([...] or empty)
    3. Top-level sections whose heading is a placeholder (e.g. # [Process Name])
       — these are template format-example sections, not real content
    """

    # 1. Remove HTML comments (single-line and multi-line)
    md_text = re.sub(r'<!--.*?-->', '', md_text, flags=re.DOTALL)

    # 2. Remove placeholder table rows — every cell matches [...] or is blank/dash
    placeholder_cell = re.compile(r'^\s*\[.*?\]\s*$|^\s*—\s*$|^\s*-\s*$|^\s*$')

    def is_placeholder_row(line):
        if not line.startswith('|'):
            return False
        cells = [c.strip() for c in line.strip().strip('|').split('|')]
        return bool(cells) and all(placeholder_cell.match(c) for c in cells)

    lines = md_text.splitlines()
    md_text = '\n'.join(line for line in lines if not is_placeholder_row(line))

    # 3. Remove sections whose heading is a pure placeholder: # [...] or ## [...]
    def remove_placeholder_sections(text):
        pattern = re.compile(r'^(#{1,3})\s+\[.*?\]\s*$', re.MULTILINE)
        result = []
        pos = 0
        for m in pattern.finditer(text):
            result.append(text[pos:m.start()])
            level = len(m.group(1))
            # Skip until next heading of same or higher level
            next_m = re.search(r'^#{1,' + str(level) + r'}\s+', text[m.end():], re.MULTILINE)
            pos = m.end() + next_m.start() if next_m else len(text)
        result.append(text[pos:])
        return ''.join(result)

    md_text = remove_placeholder_sections(md_text)

    # Clean up excessive blank lines
    md_text = re.sub(r'\n{3,}', '\n\n', md_text)

    return md_text


def build_merged_markdown(docs_dir, html_svg_pairs, png_cache_dir, strings, project_type=None, content="full"):
    files = find_allowed_files(docs_dir, strings, project_type, content)
    if not files:
        print("No files to include — check PDF_ALLOWLIST in build_pdf.py")
        sys.exit(1)

    parts = [f"# {strings['pdf_title']}\n"]

    # How to use page
    parts.append(f"\n\n# {strings['how_to_use_heading']}\n\n{strings['how_to_use_body']}\n")

    parts.append(f"## {strings['toc_heading']}\n")
    toc_lines = []
    body_parts = []

    current_section = None
    for rel, abs_path, section_label in files:
        if section_label != current_section:
            body_parts.append(f"\n\n# {section_label}\n")
            toc_lines.append(f"- **{section_label}**")
            current_section = section_label

        with open(abs_path, "r", encoding="utf-8") as f:
            content = f.read()

        # Apply per-file section filter if defined
        if rel in PDF_SECTION_FILTER:
            content = filter_sections(content, PDF_SECTION_FILTER[rel])
        content = clean_for_pdf(content)
        # Replace ```plantuml blocks with rendered PNG images
        content = inject_plantuml_blocks(content, rel, docs_dir, html_svg_pairs, png_cache_dir)
        # Also inject legacy <!-- diagram: KEY --> markers (e.g. schema ERD)
        content = inject_diagrams(content, rel, docs_dir, html_svg_pairs, png_cache_dir, strings)

        # Use the first # H1 heading from the file as the title; fall back to filename.
        h1_match = re.search(r"^#\s+(.+)$", content, re.MULTILINE)
        title = h1_match.group(1).strip() if h1_match else os.path.splitext(os.path.basename(rel))[0]
        # Strip the leading H1 from the file content — the section wrapper below already provides it.
        content_body = re.sub(r"^#\s+.+\n?", "", content, count=1)
        toc_lines.append(f"  - {title}")
        body_parts.append(f"\n\n## {title}\n\n{content_body}")

    parts.append("\n".join(toc_lines))
    parts.extend(body_parts)
    return "\n".join(parts)


