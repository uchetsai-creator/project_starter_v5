VALID_PROJECT_TYPES = {
    "web-app", "cli-tool", "library", "data-pipeline",
    "ml-pipeline", "microservices", "llm-app", "iac", "mobile-app",
}

def parse_args():
    parser = argparse.ArgumentParser(
        description="Merge approved docs/ content into a single PDF.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "docs_dir",
        nargs="?",
        default="docs",
        help="Path to the docs directory (default: docs)",
    )
    parser.add_argument(
        "-o",
        dest="output_path",
        metavar="OUTPUT",
        help="Output PDF path (default: <docs_dir>/project-{documentation|spec}-<lang>.pdf)",
    )
    parser.add_argument(
        "--lang",
        default="en",
        choices=list(STRINGS),
        help="Section labels and UI text language (default: en)",
    )
    parser.add_argument(
        "--project-type",
        metavar="TYPE",
        help=(
            "Comma-separated project type(s) to filter the PDF. "
            f"Valid values: {', '.join(sorted(VALID_PROJECT_TYPES))}"
        ),
    )
    parser.add_argument(
        "--content",
        default="full",
        choices=["full", "spec"],
        help="full — all chapters; spec — Introduction, Design, Build, Deployment only (default: full)",
    )

    args = parser.parse_args()

    project_type = None
    if args.project_type is not None:
        project_type = frozenset(t.strip() for t in args.project_type.split(","))
        invalid = project_type - VALID_PROJECT_TYPES
        if invalid:
            parser.error(
                f"Unsupported project type(s): {', '.join(sorted(invalid))}. "
                f"Available: {', '.join(sorted(VALID_PROJECT_TYPES))}"
            )

    output_path = args.output_path
    if not output_path:
        suffix = "spec" if args.content == "spec" else "documentation"
        output_path = os.path.join(args.docs_dir, f"project-{suffix}-{args.lang}.pdf")

    return args.docs_dir, output_path, args.lang, project_type, args.content


def main():
    docs_dir, output_path, lang, project_type, content = parse_args()
    strings = STRINGS[lang]

    if not os.path.isdir(docs_dir):
        print(f"Directory not found: {docs_dir}")
        sys.exit(1)

    png_cache_dir = os.path.join(docs_dir, ".pdf_build_cache")
    if os.path.isdir(png_cache_dir):
        shutil.rmtree(png_cache_dir)
    os.makedirs(png_cache_dir, exist_ok=True)

    if project_type:
        print(f"Project type: {' + '.join(sorted(project_type))}")
    if content != "full":
        print(f"Content mode: {content}")

    # Legacy: find manually-generated SVG/HTML pairs (e.g. schema ERD)
    html_svg_pairs = find_html_svg_pairs(docs_dir)
    # New: extract and render all ```plantuml blocks from markdown files
    plantuml_pairs = find_plantuml_diagrams(docs_dir, png_cache_dir, project_type, content)
    html_svg_pairs.update(plantuml_pairs)
    print(f"Found {len(html_svg_pairs)} diagram(s): {list(html_svg_pairs.keys())}")
    print(f"Language: {lang}")

    merged_md = build_merged_markdown(docs_dir, html_svg_pairs, png_cache_dir, strings,
                                      project_type=project_type, content=content)

    md_html = markdown.markdown(
        merged_md,
        extensions=["tables", "fenced_code", "toc", "nl2br"]
    )

    full_html = f"<html><head><meta charset='utf-8'></head><body>{md_html}</body></html>"

    HTML(string=full_html, base_url=docs_dir).write_pdf(
        output_path,
        stylesheets=[CSS(string=build_css(lang))]
    )

    print(f"Done: {output_path}")


if __name__ == "__main__":
    main()
